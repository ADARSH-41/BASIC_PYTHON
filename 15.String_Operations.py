#String Operations
str = 'Hello World!'
print('The String is : ',str)
print(str[0]) #prints the character at the 0th index
#Slicing operator ':'
print(str[2:5]) #prints the string starting from the index 2 and ends at index 4

print(str[2:]) #prints the string starting from the index 2 to the rest

#repeatation operator '*'
print(str*2) #repeats the string two times

#concatenation operator '+'
print(str+'TEST')