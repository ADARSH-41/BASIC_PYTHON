list = ['abcd',786,2.23,'john',70.2]
tinylist = [123,'john']

#to print the complete list
print(list,tinylist)


#to print element at zero index
print(list[0])

#to slice list starting from 2nd element (i.e index 1) to 3rd element (i.e index 2)
print(list[1:3])

#to slice the list starting from 3rd element (i.e. index 2)
print(list[2:])

#to repeat a list two times
print(tinylist*2)

#to print concatenated lists
print(list+tinylist)
